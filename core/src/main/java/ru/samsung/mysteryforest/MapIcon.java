package ru.samsung.mysteryforest;

public class MapIcon {
    float x, y, width, height; // параметры иконки

    // конструктор класса
    public MapIcon(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
